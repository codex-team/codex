             <h4>Джон Сноу, 25.10.2018</h4>
             <img  src="../../../../public/img/android_cat.jpg"><p>В этой статье мы рассмотрим три самых важных изменения в новом Android, которые не могут быть проигнорированы ни одним разработчиком, который поставил у себя в проекте targetSdk = 23 и выше. Doze Mode — режим «отключки», в который переходят все устройства на Marshmallow после некоторого времени обездвижения без зарядки. App Standby — автоматическое лишение приложений доступа к ресурсам устройства, всех которые давно не открывал пользователь. Runtime Permissions — новая модель запроса разрешений. Теперь мы, как разработчики, каждый раз обращаясь, например, к микрофону устройства, должны проверять, есть ли у нашего приложения разрешение на доступ к нему. </p> 
